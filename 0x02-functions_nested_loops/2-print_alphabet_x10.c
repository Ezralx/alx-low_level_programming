#include "main.h"

/**
 * print_alphabet_x10 - a function that prints the alphabet
 * in lowercase, followed by a new line
 *
 * Return:Always 0.
 */
void print_alphabet_x10(void)
{
	char ch;
	int i;

	for (i = 0; i <= 0; i++)
	{
		while (ch = 'a' && ch <= 'z')
		{
			_putchar(ch);
		ch++;
		}
			_putchar('\n');
	}
}

